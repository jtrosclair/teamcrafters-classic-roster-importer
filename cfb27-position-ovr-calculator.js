/**
 * Calculates the most favorable CFB 27 archetype OVR for one position.
 * Returns null when the position is unsupported or no weight calculation is possible.
 */
function calculateCfb27PositionRating(
  position,
  attributes,
) {
  const definition = findPositionDefinition(position);
  if (!definition) return null;

  let best = null;

  for (const archetype of ARCHETYPE_WEIGHTS) {
    if (archetype.position !== definition.weightPosition) continue;

    const overall = calculateArchetypeOverall(attributes, archetype);
    if (overall === null || (best !== null && best.overall >= overall)) continue;

    const archetypeId = ARCHETYPE_IDS[archetype.archetype];
    if (archetypeId === undefined) continue;

    best = {
      position: definition.position,
      overall,
      archetype: archetypeId,
    };
  }

  return best;
}

/** Calculates the best archetype OVR for every supported out-of-position group. */
function calculateAllCfb27PositionRatings(
  attributes,
) {
  return POSITION_DEFINITIONS.flatMap((definition) => {
    const result = calculateCfb27PositionRating(definition.position, attributes);
    return result ? [result] : [];
  });
}

function findPositionDefinition(position) {
  const normalizedPosition = position.trim().toUpperCase();
  return POSITION_DEFINITIONS.find((definition) => definition.aliases.includes(normalizedPosition));
}

function calculateArchetypeOverall(
  attributes,
  archetype,
) {
  const { desiredLow, desiredHigh, maxRating, sum, weights } = archetype;
  if (
    !Number.isFinite(desiredLow) ||
    !Number.isFinite(desiredHigh) ||
    desiredHigh === desiredLow ||
    !Number.isFinite(maxRating) ||
    !Number.isFinite(sum) ||
    sum === 0
  ) {
    return null;
  }

  let weightedSum = 0;
  const ratingRange = desiredHigh - desiredLow;

  for (const [weightName, weight] of Object.entries(weights)) {
    const attribute = WEIGHT_TO_ATTRIBUTE[weightName];
    const rating = Number(attributes[attribute]);
    if (!attribute || !Number.isFinite(weight) || weight === 0 || !Number.isFinite(rating)) {
      continue;
    }

    weightedSum += ((rating - desiredLow) / ratingRange) * (weight / sum);
  }

  return Math.floor(Math.max(0, Math.min(weightedSum * maxRating, maxRating)) + 0.5);
}


